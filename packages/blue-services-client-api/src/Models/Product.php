<?php
namespace Bkrol\ClientApi\Models;

use Bkrol\ClientApi\Builders\ProductBuilderInterface;

class Product implements ProductBuilderInterface
{
    private $id;
    private $name;
    private $amount;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId(int $id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getAmount()
    {
        return $this->amount;
    }

    /**
     * @param mixed $amount
     */
    public function setAmount(int $amount)
    {
        $this->amount = $amount;
    }


}